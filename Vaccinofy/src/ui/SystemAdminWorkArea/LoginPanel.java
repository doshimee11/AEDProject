    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String userName = txtUsername.getText();
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);
        
        UserAccount userAccount = system.getUserAccountDirectory().authenticateUserAccount(userName, password);
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        Network n = new Network();
        
        //Step1: Check in the system user account directory if you have the user
        if (userAccount == null) {
            //Step2: Go inside each network to check each enterprise
            for (Network network : system.getNetworkDirectory()) {
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterprisesDirectory()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUserAccount(userName, password);
                    if (userAccount == null) {
                        //Step3: Check against each organization inside that enterprise
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationDirectory()) {
                            userAccount = organization.getUserAccountDirectory().authenticateUserAccount(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                    } else {
                        inEnterprise = enterprise;
                        n = network;
                        break;
                    }
                    if (inOrganization != null) {
                        n = network;
                        break;
                    }
                }
                if (inEnterprise != null) {
                    n = network;
                    break;
                }
            }
        }
        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails !!!");
            jPanel2.removeAll();
            txtUsername.setText(null);
            txtPassword.setText(null);
            return;
        } 
        else {
            JFrame topFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
            MainJFrame mj = (MainJFrame) topFrame;
            mj.getContentPane().removeAll();
            
//            CardLayout layout = (CardLayout) jPanel2.getLayout();
//            jPanel2.add("workArea", userAccount.getRole().createWorkArea(jPanel2, userAccount, inOrganization, inEnterprise, system));
//            layout.next(jPanel2);
//            
            JPanel createWorkArea = userAccount.getRole().createWorkArea(jPanel2, userAccount, inOrganization, inEnterprise, system);
            mj.getContentPane().add(createWorkArea);
            mj.revalidate();
            mj.pack();
            mj.setSize(1200, 1000);
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed


